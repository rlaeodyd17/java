package control;

public class IfEx1 {

	public static void main(String[] args) {
		
		//분기문이란 코딩 어떤 시점의 조건시에 따라서 다르게 수행할 일이 있을 때 나눠서 작동하는 문장
		
		//분기문의 종류
		//1) if문
		
		//2) switch문 
		
		//if(조건식) {
		//	조건식이 true일때 수행되는 구문
		//}
		
		boolean b = false;
		
		if(b) {
			//b가 true일때 동작
			System.out.println("참!");
		}else {
			//b가 false일때 동작
			System.out.println("거짓!");
		}
		
		System.out.println("Program finished !");
		
	}//main() end
	
}//IfEx1 end

